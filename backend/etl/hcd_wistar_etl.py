def _confidence(n_studies: int, n_animals: int) -> str:
    """Assign confidence tier based on sample size.

    Dual criterion (studies AND animals) because multi-study pooled data has
    within-study correlation: 100 animals from 1 study is less informative
    than 100 animals from 20 studies.

    Thresholds:
      HIGH (>=20 studies, >=100 animals): FDA/ICH recommended minimum for
        a robust HCD database is 20 studies (ICH S1B(R1) Q&A); >=100 animals
        gives >99% power to detect a 0.5 SD shift in the reference mean.
      MODERATE (>=5 studies, >=30 animals): minimum for meaningful
        cross-study variability; 30 animals = CLT threshold for normal
        approximation of the sampling distribution.
      LOW: below both thresholds — use with caution.

    Differs from hcd_dog_etl._confidence(n) which uses a single-dimension
    criterion because the Choi 2011 dog data is from one colony (study count
    is always 1).
    """
    if n_studies >= 20 and n_animals >= 100:
        return "HIGH"
    if n_studies >= 5 and n_animals >= 30:
        return "MODERATE"
    return "LOW"


def _migrate_schema(conn: sqlite3.Connection) -> None:
    """Add species/source/confidence columns and hcd_om_ratios table if missing."""
    existing = {r[1] for r in conn.execute("PRAGMA table_info(hcd_aggregates)")}
    for col_name, col_type in [
        ("species", "TEXT DEFAULT 'RAT'"),
        ("source", "TEXT"),
        ("confidence", "TEXT"),
    ]:
        if col_name not in existing:
            conn.execute(
                f"ALTER TABLE hcd_aggregates ADD COLUMN {col_name} {col_type}"
            )
            log.info("Added column %s to hcd_aggregates", col_name)

    # OBO/OBR ratio table (organ/body weight and organ/brain weight ratios)
    conn.executescript("""
        CREATE TABLE IF NOT EXISTS hcd_om_ratios (
            strain TEXT NOT NULL,
            sex TEXT NOT NULL,
            organ TEXT NOT NULL,
            duration_category TEXT NOT NULL,
            ratio_type TEXT NOT NULL,  -- 'OBO' or 'OBR'
            n INTEGER NOT NULL,
            mean REAL NOT NULL,
            sd REAL NOT NULL,
            p5 REAL,
            p95 REAL,
            min_val REAL,
            max_val REAL,
            lower_2sd REAL NOT NULL,
            upper_2sd REAL NOT NULL,
            study_count INTEGER NOT NULL,
            source TEXT,
            confidence TEXT,
            PRIMARY KEY (strain, sex, organ, duration_category, ratio_type)
        );
    """)
    conn.commit()


def _load_inotiv(conn: sqlite3.Connection) -> int:
    """Insert Inotiv RccHan:WIST data into hcd_aggregates. Returns row count."""
    # Clear previous Inotiv data (idempotent)
    deleted = conn.execute(
        "DELETE FROM hcd_aggregates WHERE source = 'INOTIV_RCCHAN'"
    ).rowcount
    if deleted > 0:
        log.info("Cleared %d existing INOTIV_RCCHAN rows", deleted)

    # Also clear old NTP Wistar Han aggregates that will be superseded
    # (keep individual animal records in animal_organ_weights for percentile ranking)
    deleted_ntp = conn.execute(
        "DELETE FROM hcd_aggregates WHERE strain = 'WISTAR HAN' "
        "AND (source IS NULL OR source = 'NTP_DTT')"
    ).rowcount
    if deleted_ntp > 0:
        log.info("Cleared %d superseded NTP Wistar Han aggregate rows", deleted_ntp)

    count = 0
    for organ, age, sex, n_studies, n_animals, mean, std, min_val, max_val, p05, p95 in _INOTIV_DATA:
        if age not in _AGE_TO_DURATION:
            continue  # 13-18wk rows: retained in data for reference, not loaded

        duration_cat = _AGE_TO_DURATION[age]
        lower_2sd = round(mean - 2 * std, 6)
        upper_2sd = round(mean + 2 * std, 6)
        confidence = _confidence(n_studies, n_animals)

        conn.execute(
            """INSERT OR REPLACE INTO hcd_aggregates
               (strain, sex, organ, duration_category, n, mean, sd,
                p5, p25, median, p75, p95, min_val, max_val,
                lower_2sd, upper_2sd, study_count,
                species, source, confidence)
               VALUES (?, ?, ?, ?, ?, ?, ?,
                       ?, NULL, NULL, NULL, ?, ?, ?,
                       ?, ?, ?,
                       'RAT', 'INOTIV_RCCHAN', ?)""",
            (
                "WISTAR HAN", sex, organ, duration_cat,
                n_animals, round(mean, 6), round(std, 6),
                round(p05, 6), round(p95, 6),
                round(min_val, 6), round(max_val, 6),
                lower_2sd, upper_2sd, n_studies,
                confidence,
            ),
        )
        count += 1

    conn.commit()
    return count


def _load_ratios(conn: sqlite3.Connection) -> tuple[int, int]:
    """Insert OBO and OBR ratio data into hcd_om_ratios. Returns (obo_count, obr_count)."""
    deleted = conn.execute(
        "DELETE FROM hcd_om_ratios WHERE source = 'INOTIV_RCCHAN'"
    ).rowcount
    if deleted > 0:
        log.info("Cleared %d existing INOTIV_RCCHAN ratio rows", deleted)

    obo_count = obr_count = 0
    for ratio_type, data in [("OBO", _INOTIV_OBO_DATA), ("OBR", _INOTIV_OBR_DATA)]:
        for organ, age, sex, n_studies, n_animals, mean, std, min_val, max_val, p05, p95 in data:
            if age not in _AGE_TO_DURATION:
                continue
            duration_cat = _AGE_TO_DURATION[age]
            conn.execute(
                """INSERT OR REPLACE INTO hcd_om_ratios
                   (strain, sex, organ, duration_category, ratio_type,
                    n, mean, sd, p5, p95, min_val, max_val,
                    lower_2sd, upper_2sd, study_count, source, confidence)
                   VALUES (?, ?, ?, ?, ?,
                           ?, ?, ?, ?, ?, ?, ?,
                           ?, ?, ?, 'INOTIV_RCCHAN', ?)""",
                (
                    "WISTAR HAN", sex, organ, duration_cat, ratio_type,
                    n_animals, round(mean, 6), round(std, 6),
                    round(p05, 6), round(p95, 6),
                    round(min_val, 6), round(max_val, 6),
                    round(mean - 2 * std, 6), round(mean + 2 * std, 6),
                    n_studies, _confidence(n_studies, n_animals),
                ),
            )
            if ratio_type == "OBO":
                obo_count += 1
            else:
                obr_count += 1

    conn.commit()
    return obo_count, obr_count


def build(db_path: Path | None = None) -> None:
    """Run ETL: load Inotiv RccHan:WIST organ weight data."""
    path = db_path or HCD_DB_PATH
    if not path.exists():
        print(f"ERROR: hcd.db not found at {path}")
        print("Run: python -m etl.hcd_etl build   (to create the base DB first)")
        sys.exit(1)

    conn = sqlite3.connect(str(path))
    try:
        _migrate_schema(conn)
        count = _load_inotiv(conn)
        obo_count, obr_count = _load_ratios(conn)
        print(f"Loaded {count} OWT + {obo_count} OBO + {obr_count} OBR rows")

        # Summary
        organs = [r[0] for r in conn.execute(
            "SELECT DISTINCT organ FROM hcd_aggregates "
            "WHERE strain = 'WISTAR HAN' AND source = 'INOTIV_RCCHAN' ORDER BY organ"
        )]
        durations = [r[0] for r in conn.execute(
            "SELECT DISTINCT duration_category FROM hcd_aggregates "
            "WHERE strain = 'WISTAR HAN' AND source = 'INOTIV_RCCHAN' ORDER BY duration_category"
        )]
        print(f"  Organs ({len(organs)}): {', '.join(organs)}")
        print(f"  Duration categories: {', '.join(durations)}")
    finally:
        conn.close()


def info(db_path: Path | None = None) -> None:
    """Show Wistar Han OM HCD coverage summary."""
    path = db_path or HCD_DB_PATH
    if not path.exists():
        print(f"hcd.db not found at {path}")
        return

    conn = sqlite3.connect(str(path))
    try:
        # All Wistar Han aggregates
        rows = conn.execute(
            """SELECT organ, sex, duration_category, n, mean, sd, study_count,
                      lower_2sd, upper_2sd, p5, p95, source, confidence
               FROM hcd_aggregates
               WHERE strain = 'WISTAR HAN'
               ORDER BY organ, sex, duration_category"""
        ).fetchall()

        print(f"Wistar Han OM HCD: {len(rows)} aggregate entries")
        print(f"\n{'Organ':20s} {'Sex':3s} {'Duration':15s} {'N':>6s} {'Studies':>7s} "
              f"{'Mean':>8s} {'SD':>8s} {'[2SD low':>9s} {'2SD hi]':>9s} "
              f"{'P05':>8s} {'P95':>8s} {'Source':15s} {'Conf':8s}")
        print("-" * 135)

        for r in rows:
            organ, sex, dur, n, mean, sd, sc, lo, hi, p5, p95, src, conf = r
            p5_str = f"{p5:.4f}" if p5 is not None else "N/A"
            p95_str = f"{p95:.4f}" if p95 is not None else "N/A"
            print(f"{organ:20s} {sex:3s} {dur:15s} {n:6d} {sc:7d} "
                  f"{mean:8.4f} {sd:8.4f} {lo:9.4f} {hi:9.4f} "
                  f"{p5_str:>8s} {p95_str:>8s} {(src or 'N/A'):15s} {(conf or 'N/A'):8s}")

        # Coverage summary
        organs = sorted(set(r[0] for r in rows))
        durations = sorted(set(r[2] for r in rows))
        print(f"\nOrgans ({len(organs)}): {', '.join(organs)}")
        print(f"Duration categories: {', '.join(durations)}")

        # Compare with what existed before
        ntp_count = sum(1 for r in rows if r[11] != "INOTIV_RCCHAN")
        ino_count = sum(1 for r in rows if r[11] == "INOTIV_RCCHAN")
        print(f"\nNTP-sourced: {ntp_count}, Inotiv-sourced: {ino_count}")

    finally:
        conn.close()


def main() -> None:
    parser = argparse.ArgumentParser(
        description="Inotiv RccHan:WIST organ weight HCD ETL"
    )
    parser.add_argument("command", choices=["build", "info"])
    parser.add_argument("--db", type=Path, default=None, help="Custom hcd.db path")
    args = parser.parse_args()

    logging.basicConfig(level=logging.INFO, format="%(levelname)s: %(message)s")

    if args.command == "build":
        build(args.db)
    elif args.command == "info":
        info(args.db)


if __name__ == "__main__":
    main()
